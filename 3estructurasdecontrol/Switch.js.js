// sentencias switch

let nota = 2;

switch (nota) {
    case 5:
        console.log("Buen trabajo sobresaliente");
        break;
    case 4:
        console.log("bien pasaste")
        break;
    case 3:
        console.log("Apenas y pasaste");
        break;
    case 2: 
    console.log("Reprobaste por poco");
        break;
    case 1:
        console.log("No estudiaste");
        break;

        default:
            console.log("Error");
            break;

}